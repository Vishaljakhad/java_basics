package forloop;

//While loop is called entry controlled loop
// it verifies the condition before executing the block
public class whileloop {
    public static void main(String[] args) {
        int i = 0;   // initiliazation part is outside
        while (i < 10) {      //while loop ke ander tbhi jaskty hai tb conditon true hogi
            System.out.println(i);
            i++;
        }
    }
}

