package increment_decrement;

public class increment_decrementbyinput {

    public static void main(String[] args) {
        int a =10;
        System.out.println(++a);
        System.out.println(a);


        //POST increment
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
